const name = 'Gabriel'

// Funções
const sayHi = () => 'Oi'

const greet = sayHi()
console.log(greet)

// Métodos
const nameInUpperCase = name.toUpperCase()
console.log(nameInUpperCase)

/*
  - Funções e métodos são a mesma coisa. Elas se diferem na forma em que são declaradas e invocadas.
  - Métodos são funções associadas a objetos. 
*/